package com.robogenies.Week01.excercise;

public class Swap
{

	public static void main(String args[])
	{
		int x=10;
		int y=20;
		int z=0;

		System.out.println("X:"+x);
		System.out.println("Y:"+y);

		
		//temp var (z)
		//z=10;
		z=x;

		//x=20;
		x=y;

		//y=10;
		y=z;

		System.out.println("X:"+x);
		System.out.println("Y:"+y);
		
				

	}

}
